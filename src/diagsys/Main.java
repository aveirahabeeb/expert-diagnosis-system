     public void  DisablePRbtn(){
   
    try{
            Diagnosis  Diagnosis = new Diagnosis();
            
            conn = SqlConnect.conDB();
               st = conn.createStatement();
                    rs = st.executeQuery("select UserName from users where UserName ='"+userlbl.getText()+"' ");
             if (rs.next()){
        
            
                Diagnosis.PRtable.setVisible(false);
                Diagnosis.setVisible(true);
               
                }else{  
                        Diagnosis.setVisible(true);}
             
             }catch (InstantiationException | IllegalAccessException  | SQLException |IOException | ClassNotFoundException ex) {
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            }finally{
            if (conn!=null){
                try {
                     rs.close();
                        st.close();
                            conn.close();
                        
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this,ex.getMessage());
                }}
            }
   
   }
   

