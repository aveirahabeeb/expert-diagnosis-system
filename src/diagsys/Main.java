    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        if(jComboBox1.getSelectedIndex()== 0){
            return  ;
        }

        if(jComboBox1.getSelectedItem().equals("Blue")){

            Diag_Menu.setForeground(Color.blue);
            PR_Menu.setForeground(Color.blue);
            DN_menu.setForeground(Color.blue);

        }
        else  if(jComboBox1.getSelectedItem().equals("Default")){

            Diag_Menu.setForeground(Color.black);
            PR_Menu.setForeground(Color.black);
           DN_menu.setForeground(Color.black);

        }

        else  if(jComboBox1.getSelectedItem().equals("Red")){

            Diag_Menu.setForeground(Color.red);
            PR_Menu.setForeground(Color.red);
            DN_menu.setForeground(Color.red);

        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

